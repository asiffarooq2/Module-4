// JavaScript Math Object Example

console.log("PI:", Math.PI);
console.log("Square Root of 64:", Math.sqrt(64));
console.log("2 raised to power 3:", Math.pow(2, 3));
console.log("Rounded Value:", Math.round(4.7));
console.log("Floor Value:", Math.floor(4.7));
console.log("Ceil Value:", Math.ceil(4.2));
console.log("Maximum Value:", Math.max(10, 20, 30));
console.log("Minimum Value:", Math.min(10, 20, 30));
console.log("Random Number:", Math.random());