// Built-in Math Object in JavaScript

let number = 25.7;

console.log("Square Root:", Math.sqrt(number));
console.log("Power:", Math.pow(5, 2));
console.log("Round:", Math.round(number));
console.log("Floor:", Math.floor(number));
console.log("Ceil:", Math.ceil(number));
console.log("Random Number:", Math.random());
console.log("Maximum:", Math.max(10, 20, 30));
console.log("Minimum:", Math.min(10, 20, 30));